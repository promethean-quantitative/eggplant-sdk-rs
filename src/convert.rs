//! negRisk position operations through the relayer: **convert**, **merge**,
//! **redeem**, and **split**, plus a planning engine that turns raw
//! balances into minimal relayer submissions.
//!
//! Layers, from pure to orchestrated:
//!
//! 1. **Calldata builders** ([`build_convert_calldata`] and friends) — pure
//!    ABI encoding against the negRisk adapter / CTF, always available.
//! 2. **Planner** ([`gas_chunks`], [`plan_calls`], [`ConvertTier`],
//!    [`EventPlan`]) — pure decomposition of balances into an ordered,
//!    gas-budgeted call list.
//! 3. **Engine** (`rpc` feature: [`plan_jobs`], [`process_jobs`],
//!    [`convert_worker`], the wrap helpers) — reads live balances over RPC,
//!    submits through [`RelayerClient`](crate::relayer::RelayerClient),
//!    retries wallet-busy, and wraps the freed USDC.e to pUSD.
//!
//! The negRisk math in one line each: **merge** burns YES+NO on one leg for
//! $1; **convert** burns NO across `k` legs of one event, minting YES on the
//! complement and freeing `(k−1)·amount`; **split** is merge's inverse
//! (collateral → YES+NO); **redeem** collects a resolved market's payout.
