    /// Outcome of one convert job, echoed per consumed job.
    #[derive(Debug)]
    pub struct ConvertResult {
        pub slug: String,
        pub amount_raw: U256,
        pub n_legs: usize,
        pub success: bool,
        /// Set when the failure was relayer quota exhaustion — retry these
        /// on a fixed backoff (they self-terminate once the quota frees);
        /// cap retries on non-quota failures.
        pub quota_blocked: bool,
        /// Echo of the job's `attempts` for the caller's retry cap.
        pub attempts: u32,
        pub detail: String,
    }

    /// Remaining fill-settle wait for the youngest job in the batch:
    /// `fill_delay` measured from each job's `queued_at`, so a job that
    /// already aged in the channel doesn't wait the full delay over again.
    fn fill_residual(batch: &[ConvertJob], now: Instant, fill_delay: Duration) -> Duration {
        batch
            .iter()
            .map(|j| fill_delay.saturating_sub(now.saturating_duration_since(j.queued_at)))
            .max()
            .unwrap_or(Duration::ZERO)
    }

    /// First occurrence per slug wins (same slug ⇒ same legs); input order
    /// kept.
    fn dedup_by_slug(batch: &[ConvertJob]) -> Vec<&ConvertJob> {
        let mut seen = HashSet::new();
        batch
            .iter()
            .filter(|j| seen.insert(j.slug.as_str()))
            .collect()
    }

    /// Background merge/convert worker: drains queued jobs into one
    /// coalesced cycle per pass.
    ///
    /// Each pass waits out the youngest job's fill-settle delay, drains
    /// whatever else queued meanwhile (a burst of fills becomes one shared
    /// cycle instead of N serialized ones), dedups by slug, and runs
    /// [`process_jobs`] over the distinct events. Exactly one
    /// [`ConvertResult`] is sent per *consumed* job — duplicates included —
    /// so callers that count queued jobs and decrement per result never
    /// stall on a missing result.
    pub async fn convert_worker<S: Signer + Sync>(
        mut rx: mpsc::Receiver<ConvertJob>,
        signer: S,
        relayer: RelayerClient,
        rpc_url: String,
        proxy_wallet: Address,
        result_tx: mpsc::UnboundedSender<ConvertResult>,
        delays: ConvertDelays,
    ) {
        tracing::info!(%proxy_wallet, "convert worker started");

        while let Some(first) = rx.recv().await {
            let mut batch = vec![first];
            tokio::time::sleep(fill_residual(
                &batch,
                Instant::now(),
                delays.fill_to_convert,
            ))
            .await;
            // Single drain: jobs that arrived during the wait join this
            // cycle; later arrivals start the next pass immediately after
            // this one resolves.
            while let Ok(job) = rx.try_recv() {
                batch.push(job);
            }
            // Just-drained jobs may be younger than the fill delay; top up so
            // every job's fills have settled on-chain before the balance read.
            tokio::time::sleep(fill_residual(
                &batch,
                Instant::now(),
                delays.fill_to_convert,
            ))
            .await;

            let outcome = {
                let uniques = dedup_by_slug(&batch);
                tracing::info!(
                    jobs = batch.len(),
                    events = uniques.len(),
                    "convert cycle starting"
                );
                process_jobs(&uniques, &signer, &relayer, &rpc_url, proxy_wallet, delays).await
            };

            match outcome {
                Ok(detail) => {
                    tracing::info!(%detail, "convert cycle complete");
                    for job in batch {
                        let _ = result_tx.send(ConvertResult {
                            slug: job.slug,
                            amount_raw: job.amount_raw,
                            n_legs: job.legs.len(),
                            success: true,
                            quota_blocked: false,
                            attempts: job.attempts,
                            detail: detail.clone(),
                        });
                    }
                }
                Err(Error::RelayerQuotaExhausted { resets_in_secs }) => {
                    // The relayer's hint is unreliable — logged for forensics
                    // only; the caller retries on the fixed backoff, and this
                    // sleep is an anti-spin damp.
                    tracing::warn!(resets_in_secs, "relayer quota exhausted");
                    for job in batch {
                        let _ = result_tx.send(ConvertResult {
                            slug: job.slug,
                            amount_raw: job.amount_raw,
                            n_legs: job.legs.len(),
                            success: false,
                            quota_blocked: true,
                            attempts: job.attempts,
                            detail: "quota exhausted".to_string(),
                        });
                    }
                    tokio::time::sleep(delays.retry_backoff).await;
                }
                Err(e) => {
                    tracing::error!(error = %e, "convert cycle failed");
                    let detail = e.to_string();
                    for job in batch {
                        let _ = result_tx.send(ConvertResult {
                            slug: job.slug,
                            amount_raw: job.amount_raw,
                            n_legs: job.legs.len(),
                            success: false,
                            quota_blocked: false,
                            attempts: job.attempts,
                            detail: detail.clone(),
                        });
                    }
                    tokio::time::sleep(delays.retry_backoff).await;
                }
            }
        }
    }

    async fn submit_and_settle<S: Signer + Sync>(
        signer: &S,
        relayer: &RelayerClient,
        wallet: Address,
        calls: &[DepositWalletCall],
        label: &str,
        settle: Duration,
    ) -> Result<String, Error> {
        let resp = relayer
            .submit_deposit_wallet_batch(signer, wallet, POLYGON, calls)
            .await?;
        tracing::info!(tx_id = %resp.transaction_id, "{label} submitted, waiting for settlement");
        tokio::time::sleep(settle).await;
        Ok(resp.transaction_id)
    }

    /// Submit a relayer batch and wait for settlement, retrying while the
    /// relayer reports the wallet busy.
    ///
    /// A "wallet busy: active action exists" rejection means a prior action
    /// for this wallet is still settling and *nothing was submitted*, so
    /// retrying is safe — there is no double-submission risk. The condition
    /// clears once the prior action lands, so wait `settle` and try again,
    /// up to `max_retries` times. Any non-busy error returns immediately.
    pub async fn submit_and_settle_with_busy_retry<S: Signer + Sync>(
        signer: &S,
        relayer: &RelayerClient,
        wallet: Address,
        calls: &[DepositWalletCall],
        label: &str,
        settle: Duration,
        max_retries: u32,
    ) -> Result<String, Error> {
        let mut attempt = 0;
        loop {
            match submit_and_settle(signer, relayer, wallet, calls, label, settle).await {
                Ok(tx_id) => return Ok(tx_id),
                Err(e) if e.is_wallet_busy() && attempt < max_retries => {
                    attempt += 1;
                    tracing::warn!(
                        %label,
                        attempt,
                        max_retries,
                        wait_secs = settle.as_secs(),
                        "submit deferred: wallet busy, waiting then retrying"
                    );
                    tokio::time::sleep(settle).await;
                }
                Err(e) => return Err(e),
            }
        }
    }

    /// Flatten the legs' token ids into one `balanceOfBatch` query: per leg,
    /// the NO token id, then the YES token id when known.
    /// [`unpack_balances`] walks the same layout back out.
    fn batch_token_ids(legs: &[ConvertLeg]) -> Vec<U256> {
        let mut ids = Vec::with_capacity(legs.len() * 2);
        for leg in legs {
            ids.push(leg.no_token_id);
            if let Some(yid) = leg.yes_token_id {
                ids.push(yid);
            }
        }
        ids
    }

    /// Number of token ids [`batch_token_ids`] emits for these legs.
    fn token_count(legs: &[ConvertLeg]) -> usize {
        legs.iter()
            .map(|l| 1 + usize::from(l.yes_token_id.is_some()))
            .sum()
    }

    /// Inverse of [`batch_token_ids`]: split the flat balance array back
    /// into per-leg `(no, yes)` pairs. Legs without a YES token id get a
    /// zero YES balance. Errors when the array length doesn't match the
    /// queried layout.
    fn unpack_balances(legs: &[ConvertLeg], balances: &[U256]) -> Result<Vec<(U256, U256)>, Error> {
        fn truncated() -> Error {
            Error::InvalidData("balanceOfBatch returned fewer balances than queried".into())
        }

        let mut cursor = balances.iter().copied();
        let mut pairs = Vec::with_capacity(legs.len());
        for leg in legs {
            let no_bal = cursor.next().ok_or_else(truncated)?;
            let yes_bal = if leg.yes_token_id.is_some() {
                cursor.next().ok_or_else(truncated)?
            } else {
                U256::ZERO
            };
            pairs.push((no_bal, yes_bal));
        }
        if cursor.next().is_some() {
            return Err(Error::InvalidData(
                "balanceOfBatch returned more balances than queried".into(),
            ));
        }
        Ok(pairs)
    }

    /// Split one combined multi-job `balanceOfBatch` result back into
    /// per-job, per-leg `(no, yes)` pairs (jobs were concatenated in order).
    fn unpack_multi(
        jobs: &[&ConvertJob],
        balances: &[U256],
    ) -> Result<Vec<Vec<(U256, U256)>>, Error> {
        let mut out = Vec::with_capacity(jobs.len());
        let mut offset = 0_usize;
        for job in jobs {
            let n = token_count(&job.legs);
            let slice = balances.get(offset..offset + n).ok_or_else(|| {
                Error::InvalidData("balanceOfBatch returned fewer balances than queried".into())
            })?;
            out.push(unpack_balances(&job.legs, slice)?);
            offset += n;
        }
        if offset != balances.len() {
            return Err(Error::InvalidData(
                "balanceOfBatch returned more balances than queried".into(),
            ));
        }
        Ok(out)
    }

    /// Read every job's CTF balances in a single `balanceOfBatch` `eth_call`
    /// (same-block consistent across all events, one billed RPC call total).
    ///
    /// Propagates RPC errors instead of masking them as zero balances: an
    /// ERC1155 read returns 0 for an unheld token, so a masked *failed read*
    /// would silently skip converting a real position and look like "nothing
    /// to do".
    async fn read_balances_multi(
        jobs: &[&ConvertJob],
        rpc_url: &str,
        wallet: Address,
    ) -> Result<Vec<Vec<(U256, U256)>>, Error> {
        let mut ids: Vec<U256> =
            Vec::with_capacity(jobs.iter().map(|j| token_count(&j.legs)).sum());
        for job in jobs {
            ids.extend(batch_token_ids(&job.legs));
        }
        if ids.is_empty() {
            return Ok(jobs.iter().map(|_| Vec::new()).collect());
        }

        let provider = ProviderBuilder::new()
            .connect(rpc_url)
            .await
            .map_err(|e| Error::InvalidData(format!("RPC connect failed: {e}")))?;
        let ctf = IERC1155::new(CTF, &provider);

        let n = ids.len();
        let balances = ctf
            .balanceOfBatch(vec![wallet; n], ids)
            .call()
            .await
            .map_err(|e| Error::InvalidData(format!("balanceOfBatch failed: {e}")))?;

        unpack_multi(jobs, &balances)
    }

    /// Read the wallet's USDC.e balance and onramp allowance — the folded
    /// wrap's inputs.
    async fn read_wrap_snapshot(rpc_url: &str, wallet: Address) -> Result<WrapSnapshot, Error> {
        let provider = ProviderBuilder::new()
            .connect(rpc_url)
            .await
            .map_err(|e| Error::InvalidData(format!("RPC connect failed: {e}")))?;
        let usdc = IERC20::new(USDC_E, &provider);

        let balance = usdc
            .balanceOf(wallet)
            .call()
            .await
            .map_err(|e| Error::InvalidData(format!("USDC balance read failed: {e}")))?;
        let allowance = usdc
            .allowance(wallet, COLLATERAL_ONRAMP)
            .call()
            .await
            .map_err(|e| Error::InvalidData(format!("USDC allowance read failed: {e}")))?;

        Ok(WrapSnapshot { balance, allowance })
    }

    /// Read live balances and produce per-job [`super::EventPlan`]s plus the
    /// wrap snapshot — submits nothing. Drives [`process_jobs`] and
    /// read-only planning reports.
    pub async fn plan_jobs(
        jobs: &[&ConvertJob],
        rpc_url: &str,
        wallet: Address,
        single_leg_min_qty_raw: U256,
    ) -> Result<(Vec<super::EventPlan>, WrapSnapshot), Error> {
        let (balances, snapshot) = tokio::join!(
            read_balances_multi(jobs, rpc_url, wallet),
            read_wrap_snapshot(rpc_url, wallet)
        );
        let (balances, snapshot) = (balances?, snapshot?);

        let plans = jobs
            .iter()
            .zip(&balances)
            .map(|(job, pairs)| plan_event_from_balances(&job.legs, pairs, single_leg_min_qty_raw))
            .collect();

        Ok((plans, snapshot))
    }

    /// Run the full merge→convert→wrap cycle for one or more events in as
    /// few relayer submissions as possible.
    ///
    /// One balance snapshot plans everything ([`plan_jobs`]); every event's
    /// merges and convert tiers ride a single relayer submission when they
    /// fit, followed by exactly one settle wait, then one balance-read-sized
    /// wrap submission. The wrap cannot ride the batch: the relayer's
    /// pre-submission simulation evaluates each call against pre-batch
    /// state, where the proceeds don't exist yet. A failed submission (other
    /// than quota exhaustion) re-reads balances and rebuilds, absorbing
    /// mid-flight balance changes.
    pub async fn process_jobs<S: Signer + Sync>(
        jobs: &[&ConvertJob],
        signer: &S,
        relayer: &RelayerClient,
        rpc_url: &str,
        wallet: Address,
        delays: ConvertDelays,
    ) -> Result<String, Error> {
        /// Re-plans after a failed submission round (fresh balance read each
        /// time).
        const REBUILD_ATTEMPTS: usize = 2;

        let ConvertDelays {
            settle,
            wallet_busy_max_retries,
            single_leg_min_qty_raw,
            max_calls_per_submission,
            max_gas_per_submission,
            ..
        } = delays;

        let mut last_err: Option<Error> = None;
        for attempt in 0..=REBUILD_ATTEMPTS {
            if attempt > 0 {
                // A failed round may have landed earlier chunks that haven't
                // settled yet; re-reading too soon would re-plan (and
                // re-submit) their calls, which would then revert on-chain
                // against the already-burned balances.
                tokio::time::sleep(settle).await;
            }

            let outcome: Result<String, Error> = async {
                let (plans, snapshot) =
                    plan_jobs(jobs, rpc_url, wallet, single_leg_min_qty_raw).await?;
                let planned = plan_calls(&plans)?;
                if planned.is_empty() {
                    // USDC.e already sitting in the wallet still gets wrapped.
                    if snapshot.balance > U256::ZERO {
                        wrap_best_effort(signer, relayer, rpc_url, wallet, &delays, "leftover")
                            .await;
                        return Ok(format!(
                            "no-op: wrapped leftover {} USDC.e",
                            fmt_usdc(snapshot.balance)
                        ));
                    }
                    return Ok("no-op: nothing to merge/convert/wrap".into());
                }

                let merges: usize = plans
                    .iter()
                    .map(|p| {
                        p.merges.len()
                            + p.tiers.iter().map(|t| t.post_merges.len()).sum::<usize>()
                    })
                    .sum();
                let converts: usize = plans.iter().map(|p| p.tiers.len()).sum();
                let proceeds = plans.iter().fold(U256::ZERO, |acc, p| acc + p.proceeds);
                let gas_estimate: u64 = planned.iter().map(|p| p.gas).sum();
                let ranges =
                    gas_chunks(&planned, max_calls_per_submission, max_gas_per_submission);
                let n_chunks = ranges.len();
                tracing::info!(
                    events = jobs.len(),
                    merges,
                    converts,
                    proceeds = %fmt_usdc(proceeds),
                    gas_estimate,
                    chunks = n_chunks,
                    attempt,
                    "convert cycle planned"
                );

                let calls: Vec<DepositWalletCall> =
                    planned.into_iter().map(|p| p.call).collect();
                let mut tx_id = String::new();
                for (i, range) in ranges.into_iter().enumerate() {
                    let label = format!("cycle chunk {}/{n_chunks}", i + 1);
                    tx_id = submit_and_settle_with_busy_retry(
                        signer,
                        relayer,
                        wallet,
                        &calls[range],
                        &label,
                        settle,
                        wallet_busy_max_retries,
                    )
                    .await?;
                }

                // Wrap the freed USDC.e in its own submission, sized by a
                // fresh balance read (the last chunk's settle wait has
                // elapsed, so the read sees the proceeds plus any
                // pre-existing leftover).
                wrap_best_effort(signer, relayer, rpc_url, wallet, &delays, "post-batch").await;

                Ok(format!(
                    "events={} merges={merges} converts={converts} proceeds={} chunks={n_chunks} tx={tx_id}",
                    jobs.len(),
                    fmt_usdc(proceeds),
                ))
            }
            .await;

            match outcome {
                Ok(detail) => return Ok(detail),
                // The caller sleeps out the quota reset; a salvage wrap
                // submission would just burn another rejected request
                // against the same quota.
                Err(e @ Error::RelayerQuotaExhausted { .. }) => return Err(e),
                Err(e) => {
                    tracing::warn!(error = %e, attempt, "convert round failed; re-reading and rebuilding");
                    last_err = Some(e);
                }
            }
        }

        // Every round failed. Salvage: wrap whatever USDC.e the chunks that
        // did land freed, so it isn't stranded until the next cycle.
        wrap_best_effort(signer, relayer, rpc_url, wallet, &delays, "salvage").await;

        Err(last_err.unwrap_or_else(|| Error::InvalidData("convert cycle failed".into())))
    }

    /// Best-effort wrap of whatever USDC.e the wallet holds. A failure is
    /// logged, never fatal: the merges/converts that freed the cash already
    /// landed on-chain, and unwrapped USDC.e is picked up by the next
    /// cycle's leftover wrap.
    async fn wrap_best_effort<S: Signer + Sync>(
        signer: &S,
        relayer: &RelayerClient,
        rpc_url: &str,
        wallet: Address,
        delays: &ConvertDelays,
        context: &str,
    ) {
        if let Err(e) = wrap_with_busy_retry(
            signer,
            relayer,
            rpc_url,
            wallet,
            delays.wallet_busy_max_retries,
            delays.settle,
        )
        .await
        {
            tracing::warn!(error = %e, "{context} wrap failed");
        }
    }

    /// Single-event wrapper around [`process_jobs`].
    pub async fn process_job<S: Signer + Sync>(
        job: &ConvertJob,
        signer: &S,
        relayer: &RelayerClient,
        rpc_url: &str,
        wallet: Address,
        delays: ConvertDelays,
    ) -> Result<String, Error> {
        process_jobs(&[job], signer, relayer, rpc_url, wallet, delays).await
    }

    /// Submit a batch of YES+NO merges and wait for settlement.
    pub async fn process_merge<S: Signer + Sync>(
        merges: &[(B256, U256)],
        signer: &S,
        relayer: &RelayerClient,
        wallet: Address,
    ) -> Result<String, Error> {
        let calls = super::merge_calls(merges);

        let resp = relayer
            .submit_deposit_wallet_batch(signer, wallet, POLYGON, &calls)
            .await?;

        Ok(resp.transaction_id)
    }

    /// Submit a convert of NO positions and wait for settlement, returning
    /// the transaction id. Wrapping the freed USDC.e is left to the caller
    /// (via [`wrap_with_busy_retry`]), mirroring [`process_merge`].
    pub async fn process_convert<S: Signer + Sync>(
        question_ids: &[B256],
        amount_raw: U256,
        signer: &S,
        relayer: &RelayerClient,
        wallet: Address,
        settle: Duration,
    ) -> Result<String, Error> {
        let convert_data = build_convert_calldata(question_ids, amount_raw)?;
        let tx_id = submit_and_settle(
            signer,
            relayer,
            wallet,
            &[DepositWalletCall {
                target: NEG_RISK_COLLATERAL_ADAPTER,
                data: convert_data,
            }],
            "convert",
            settle,
        )
        .await?;

        Ok(tx_id)
    }

    /// Wrap the wallet's whole USDC.e balance into pUSD, including an
    /// unlimited approve when the onramp allowance falls short.
    pub async fn wrap_usdc_e<S: Signer + Sync>(
        signer: &S,
        relayer: &RelayerClient,
        rpc_url: &str,
        wallet: Address,
    ) -> Result<(), Error> {
        let snapshot = read_wrap_snapshot(rpc_url, wallet).await?;

        if snapshot.balance == U256::ZERO {
            tracing::info!("no USDC.e to wrap");
            return Ok(());
        }

        let include_approve = snapshot.allowance < snapshot.balance;
        if include_approve {
            tracing::info!(balance = %snapshot.balance, "including USDC approve in wrap batch");
        }
        tracing::info!(balance = %snapshot.balance, "wrapping USDC.e to pUSD");
        let calls = wrap_calls(wallet, snapshot.balance, include_approve);

        let resp = relayer
            .submit_deposit_wallet_batch(signer, wallet, POLYGON, &calls)
            .await?;
        tracing::info!(tx_id = %resp.transaction_id, "wrap submitted");

        Ok(())
    }

    /// Wrap USDC.e → pUSD after a merge/convert, retrying while the wallet
    /// is busy.
    ///
    /// The merge/convert just submitted can still be settling when the wrap
    /// goes out, and the relayer rejects the concurrent action with "wallet
    /// busy". The condition clears once the prior action lands on-chain, so
    /// wait `retry_wait` and try again, up to `max_retries` times.
    pub async fn wrap_with_busy_retry<S: Signer + Sync>(
        signer: &S,
        relayer: &RelayerClient,
        rpc_url: &str,
        wallet: Address,
        max_retries: u32,
        retry_wait: Duration,
    ) -> Result<(), Error> {
        let mut attempt = 0;
        loop {
            match wrap_usdc_e(signer, relayer, rpc_url, wallet).await {
                Ok(()) => return Ok(()),
                Err(e) if e.is_wallet_busy() && attempt < max_retries => {
                    attempt += 1;
                    tracing::warn!(
                        attempt,
                        max_retries,
                        wait_secs = retry_wait.as_secs(),
                        "wrap deferred: wallet busy, waiting then retrying"
                    );
                    tokio::time::sleep(retry_wait).await;
                }
                Err(e) => return Err(e),
            }
        }
    }

