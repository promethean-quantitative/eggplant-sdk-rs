/// Max question ids per on-chain `convertPositions` index set.
///
/// The proven per-call width; wider tiers are truncated with compensating
/// post-merges (see [`ConvertTier`]). Distinct from the calls-per-submission
/// cap ([`ConvertDelays::max_calls_per_submission`]).
pub const MAX_LEGS_PER_CONVERT: usize = 20;

/// Tuning for the merge/convert cycle.
#[derive(Debug, Clone, Copy)]
pub struct ConvertDelays {
    /// Wait after a fill before starting the cycle (lets fills settle
    /// on-chain before the balance read).
    pub fill_to_convert: Duration,
    /// Wait for on-chain settlement after each relayer submission.
    pub settle: Duration,
    /// Max times to retry a submission while the relayer reports the wallet
    /// busy with a prior still-settling action.
    pub wallet_busy_max_retries: u32,
    /// Minimum leftover-NO (raw, 6-dp) on a *single* leg before it's worth a
    /// convert. Below this, a lone NO leg is left in place rather than
    /// spending gas to mint YES dust for 0 USDC. Multi-leg converts ignore
    /// this.
    pub single_leg_min_qty_raw: U256,
    /// Max `DepositWallet` calls packed into one relayer submission.
    pub max_calls_per_submission: usize,
    /// Gas budget per relayer submission. Converts on big events are heavy
    /// (measured live: 6.4–7.3M each on a 48-leg event); stacking six
    /// breached Polygon's 30M block limit and the relayer rejected the whole
    /// batch as "would revert". Chunks pack greedily under this budget; a
    /// lone over-budget call still submits by itself.
    pub max_gas_per_submission: u64,
    /// Fixed backoff after a failed cycle (anti-spin damp). The relayer's
    /// 429 `resets in` hint is deliberately ignored — it reports ~3600s
    /// while the quota actually frees in well under a minute.
    pub retry_backoff: Duration,
}

impl Default for ConvertDelays {
    fn default() -> Self {
        Self {
            fill_to_convert: Duration::from_secs(5),
            settle: Duration::from_secs(5),
            wallet_busy_max_retries: 10,
            single_leg_min_qty_raw: U256::from(100_000_u64),
            max_calls_per_submission: 20,
            max_gas_per_submission: 8_000_000,
            retry_backoff: Duration::from_secs(45),
        }
    }
}

