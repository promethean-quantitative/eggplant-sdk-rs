/// One market's identifiers as string-typed API data (the shapes Gamma and
/// the Data API deliver them in).
#[derive(Debug, Clone, Copy)]
pub struct MarketIds<'a> {
    /// Hex, with or without `0x`.
    pub question_id: Option<&'a str>,
    /// Hex, with or without `0x`.
    pub condition_id: Option<&'a str>,
    /// Decimal token id.
    pub yes_token_id: Option<&'a str>,
    /// Decimal token id.
    pub no_token_id: &'a str,
}

/// Parse [`ConvertLeg`]s out of string-typed market ids.
///
/// Skips any market missing a parseable `question_id` or `no_token_id` —
/// unparseable legs are silently unplannable rather than fatal.
#[must_use]
pub fn convert_legs<'a>(markets: impl IntoIterator<Item = MarketIds<'a>>) -> Vec<ConvertLeg> {
    fn parse_b256(raw: &str) -> Option<B256> {
        raw.strip_prefix("0x").unwrap_or(raw).parse().ok()
    }

    markets
        .into_iter()
        .filter_map(|m| {
            let question_id = parse_b256(m.question_id?)?;
            let no_token_id = m.no_token_id.parse().ok()?;
            let yes_token_id = m.yes_token_id.and_then(|y| y.parse().ok());
            let condition_id = m.condition_id.and_then(parse_b256);
            Some(ConvertLeg {
                question_id,
                condition_id,
                yes_token_id,
                no_token_id,
            })
        })
        .collect()
}

/// Derive the shared negRisk event `marketId` from any question ID.
///
/// The last byte encodes the per-question index; zeroing it yields the
/// shared event identifier.
#[must_use]
pub fn market_id_from_question_id(question_id: &B256) -> B256 {
    let mut bytes = question_id.0;
    bytes[31] = 0;
    B256::from(bytes)
}

/// Compute the index-set bitmap for a set of question IDs.
///
/// Each question ID's last byte is its position index within the event.
#[must_use]
pub fn index_set(question_ids: &[B256]) -> U256 {
    let mut bits = U256::ZERO;
    for qid in question_ids {
        let idx = qid.0[31];
        bits |= U256::from(1_u64) << idx;
    }
    bits
}

/// Calldata for `NegRiskAdapter.convertPositions`: burn `amount` NO on every
/// leg in `question_ids`, minting `amount` YES on the event's complement.
pub fn build_convert_calldata(question_ids: &[B256], amount: U256) -> Result<Vec<u8>, Error> {
    if question_ids.is_empty() {
        return Err(Error::InvalidData(
            "convert requires at least one question_id".into(),
        ));
    }
    let market_id = market_id_from_question_id(&question_ids[0]);
    let idx_set = index_set(question_ids);

    Ok(INegRiskAdapterOps::convertPositionsCall {
        marketId: market_id,
        indexSet: idx_set,
        amount,
    }
    .abi_encode())
}

/// Calldata to merge `amount` YES+NO on `condition_id` back into `amount`
/// collateral, through the pUSD [`NEG_RISK_COLLATERAL_ADAPTER`].
///
/// The adapter mirrors the CTF ABI, so this is the CTF-mirror
/// `mergePositions(collateralToken, parentCollectionId, conditionId, partition, amount)`
/// with pUSD collateral, a zero parent collection, and the binary `[1, 2]`
/// partition — verified against a live UI merge (tx `0xe92073a6…`). The
/// legacy-style `mergePositions(bytes32,uint256)` overload the old adapter took
/// still exists in this adapter's bytecode but REVERTS.
#[must_use]
pub fn build_merge_calldata(condition_id: &B256, amount: U256) -> Vec<u8> {
    build_merge_calldata_ctf(COLLATERAL, condition_id, amount)
}

/// Calldata to split `amount` collateral into `amount` YES+NO on
/// `condition_id` (merge's inverse).
///
/// Goes through the pUSD [`NEG_RISK_COLLATERAL_ADAPTER`] as the CTF-mirror
/// `splitPosition(collateralToken, parentCollectionId, conditionId, partition, amount)`.
///
/// ⚠ The least-exercised call in this crate, and the only one **not** verified
/// against a live on-chain tx. The heavily exercised merge takes the exact
/// symmetric CTF-mirror form through this same adapter, so this is the
/// consistent shape — but split a dust amount first before trusting it with
/// size. Splitting also requires the adapter to be approved to pull the
/// wallet's pUSD collateral.
#[must_use]
pub fn build_split_calldata(condition_id: &B256, amount: U256) -> Vec<u8> {
    build_split_calldata_ctf(COLLATERAL, condition_id, amount)
}

/// Calldata for the plain CTF `splitPosition` (non-negRisk markets):
/// `parentCollectionId` zero and the binary partition `[1, 2]`.
#[must_use]
pub fn build_split_calldata_ctf(collateral: Address, condition_id: &B256, amount: U256) -> Vec<u8> {
    ICtfOps::splitPositionCall {
        collateralToken: collateral,
        parentCollectionId: B256::ZERO,
        conditionId: *condition_id,
        partition: vec![U256::from(1_u64), U256::from(2_u64)],
        amount,
    }
    .abi_encode()
}

/// Calldata for the plain CTF `mergePositions` (non-negRisk markets) — the
/// symmetric inverse of [`build_split_calldata_ctf`].
#[must_use]
pub fn build_merge_calldata_ctf(collateral: Address, condition_id: &B256, amount: U256) -> Vec<u8> {
    ICtfOps::mergePositionsCall {
        collateralToken: collateral,
        parentCollectionId: B256::ZERO,
        conditionId: *condition_id,
        partition: vec![U256::from(1_u64), U256::from(2_u64)],
        amount,
    }
    .abi_encode()
}

/// Calldata to redeem a resolved negRisk condition through the pUSD
/// [`NEG_RISK_COLLATERAL_ADAPTER`].
///
/// The adapter mirrors the CTF ABI, so this is the CTF-mirror
/// `redeemPositions(collateralToken, parentCollectionId, conditionId, indexSets)`
/// with pUSD collateral and a zero parent collection — verified against live
/// on-chain redeems (tx `0x2252f36d…` used `[1, 2]`, `0x7bb402a9…` used `[1]`).
/// Unlike the legacy `redeemPositions(bytes32,uint256[])` (which took explicit
/// `[yes, no]` amounts and REVERTS on this adapter), the CTF-mirror form takes
/// **index sets** and redeems the caller's *full* balance of each: `yes_amount`
/// / `no_amount` here only decide **which** sides to include (`> 0`), not how
/// much — `YES = index set 1` (outcome slot 0), `NO = index set 2` (slot 1).
/// Only meaningful once the condition is resolved (an unresolved redeem reverts
/// in the CTF); the caller gates on that and filters both-zero legs.
#[must_use]
pub fn build_redeem_calldata(condition_id: &B256, yes_amount: U256, no_amount: U256) -> Vec<u8> {
    let mut index_sets = Vec::with_capacity(2);
    if yes_amount > U256::ZERO {
        index_sets.push(U256::from(1_u64));
    }
    if no_amount > U256::ZERO {
        index_sets.push(U256::from(2_u64));
    }
    build_redeem_calldata_ctf(COLLATERAL, condition_id, &index_sets)
}

/// Calldata for the plain CTF `redeemPositions` (binary markets):
/// `parentCollectionId` zero and the outcome `index_sets` to redeem (`[1, 2]`
/// covers both slots of a binary condition).
///
/// Unlike [`build_redeem_calldata`], the CTF redeems the caller's *full*
/// balance of the given index sets (no per-token amounts) and pays out in
/// `collateral` — so pass the collateral token the condition was prepared
/// with. Only meaningful once the condition is resolved.
#[must_use]
pub fn build_redeem_calldata_ctf(
    collateral: Address,
    condition_id: &B256,
    index_sets: &[U256],
) -> Vec<u8> {
    ICtfOps::redeemPositionsCall {
        collateralToken: collateral,
        parentCollectionId: B256::ZERO,
        conditionId: *condition_id,
        indexSets: index_sets.to_vec(),
    }
    .abi_encode()
}

/// `DepositWallet` calls to redeem resolved negRisk positions, one per
/// condition.
///
/// `redeems` is `(condition_id, yes_amount, no_amount)`; every call targets
/// the shared [`NEG_RISK_COLLATERAL_ADAPTER`]. The caller filters out legs with
/// both amounts zero and gates on resolution.
#[must_use]
pub fn redeem_calls(redeems: &[(B256, U256, U256)]) -> Vec<DepositWalletCall> {
    redeems
        .iter()
        .map(|(cid, yes, no)| DepositWalletCall {
            target: NEG_RISK_COLLATERAL_ADAPTER,
            data: build_redeem_calldata(cid, *yes, *no),
        })
        .collect()
}

/// `DepositWallet` calls to merge YES+NO pairs, one per condition.
/// `merges` is `(condition_id, amount)`.
#[must_use]
pub fn merge_calls(merges: &[(B256, U256)]) -> Vec<DepositWalletCall> {
    merges
        .iter()
        .map(|(cid, amount)| DepositWalletCall {
            target: NEG_RISK_COLLATERAL_ADAPTER,
            data: build_merge_calldata(cid, *amount),
        })
        .collect()
}

/// `DepositWallet` calls to split collateral into YES+NO on negRisk
/// conditions, one per condition.
///
/// `splits` is `(condition_id, amount)`. Mirrors [`merge_calls`]; see
/// [`build_split_calldata`] for the net-new caveat.
#[must_use]
pub fn split_calls(splits: &[(B256, U256)]) -> Vec<DepositWalletCall> {
    splits
        .iter()
        .map(|(cid, amount)| DepositWalletCall {
            target: NEG_RISK_COLLATERAL_ADAPTER,
            data: build_split_calldata(cid, *amount),
        })
        .collect()
}

/// Render a raw 6-dp USDC amount as a decimal string (e.g. `12.345678`).
#[must_use]
pub fn fmt_usdc(raw: U256) -> String {
    let v: u128 = raw.try_into().unwrap_or(u128::MAX);
    format!("{}.{:06}", v / 1_000_000, v % 1_000_000)
}

/// One leg still holding NO after its YES+NO merge is netted out — the unit
/// the tier planner works over.
#[derive(Debug, Clone, PartialEq, Eq)]
struct NoHolding {
    question_id: B256,
    condition_id: Option<B256>,
    /// NO left after `min(yes, no)` is merged away (or merely offset, when
    /// the leg has no condition id to merge through).
    remaining_no: U256,
}

/// One convert call: burn `amount` NO across `question_ids`.
///
/// `post_merges` are the compensating merges for members truncated past the
/// per-call leg cap — the convert mints `amount` YES on every leg outside
/// its index set, so merging that YES against a truncated member's
/// still-held NO frees the same `$amount` the untruncated convert would
/// have. They MUST execute after this tier's convert (they consume the YES
/// it mints).
#[derive(Debug, PartialEq, Eq)]
pub struct ConvertTier {
    pub question_ids: Vec<B256>,
    pub amount: U256,
    pub post_merges: Vec<(B256, U256)>,
}

/// Deterministic full merge/convert plan for one event, computed from a
/// single balance snapshot.
#[derive(Debug, Default, PartialEq, Eq)]
pub struct EventPlan {
    /// Pre-convert YES+NO merges, `(condition_id, amount)`.
    pub merges: Vec<(B256, U256)>,
    /// Convert tiers in execution order.
    pub tiers: Vec<ConvertTier>,
    /// Exact USDC.e (raw, 6-dp) the plan frees: each merge frees its amount,
    /// a convert across `k` legs frees `(k−1)·amount`.
    pub proceeds: U256,
    /// The event's total leg count — drives the per-convert gas estimate
    /// (the adapter's convert cost scales with the whole event, not the
    /// index set).
    pub n_legs: usize,
}

/// Static merge gas estimate for chunk packing, measured live.
///
/// A probe on a 48-leg event measured merge ≈ 330k and converts 6.4–7.3M
/// (≈ ~140k per event leg — the adapter splits collateral across every
/// complement leg and merges a full set across all of the event's
/// conditions, so cost scales with the whole event, not the index set).
/// Weights carry ~25% margin: overestimating costs an extra submission;
/// underestimating gets the whole batch rejected as "would revert".
pub const MERGE_GAS: u64 = 400_000;

/// Gas estimate for one convert on an event with `n_event_legs` legs.
#[must_use]
pub fn convert_gas(n_event_legs: usize) -> u64 {
    let n = u64::try_from(n_event_legs).unwrap_or(u64::MAX);
    700_000_u64.saturating_add(175_000_u64.saturating_mul(n))
}

/// One planned `DepositWallet` call plus its gas estimate for chunk packing.
#[derive(Debug)]
pub struct PlannedCall {
    pub call: DepositWalletCall,
    pub gas: u64,
}

/// Greedy contiguous chunking under both a call-count cap and a gas budget.
///
/// Returns index ranges into the planned-call slice; concatenated they cover
/// it exactly, preserving order (which is load-bearing — see
/// [`plan_calls`]). A single call over the gas budget still gets a chunk of
/// its own: calls are atomic, and the relayer historically accepted lone
/// big-event converts.
#[must_use]
pub fn gas_chunks(
    planned: &[PlannedCall],
    max_calls: usize,
    max_gas: u64,
) -> Vec<std::ops::Range<usize>> {
    let max_calls = max_calls.max(1);
    let mut ranges = Vec::new();
    let mut start = 0_usize;
    let mut gas = 0_u64;
    for (i, p) in planned.iter().enumerate() {
        let count = i - start;
        if count > 0 && (count >= max_calls || gas.saturating_add(p.gas) > max_gas) {
            ranges.push(start..i);
            start = i;
            gas = 0;
        }
        gas = gas.saturating_add(p.gas);
    }
    if start < planned.len() {
        ranges.push(start..planned.len());
    }
    ranges
}

/// Pre-submission USDC.e snapshot driving the folded wrap.
#[derive(Debug, Clone, Copy)]
pub struct WrapSnapshot {
    pub balance: U256,
    pub allowance: U256,
}

/// Classify per-leg `(no, yes)` balances into pre-convert merges and the
/// remaining-NO holdings the tier planner works over.
///
/// A leg holding both sides merges `min(yes, no)` when it has a condition
/// id; either way the NO left over is `no − min(yes, no)` — without a
/// condition id the YES can't merge but still offsets that much NO (it is
/// never converted).
fn classify_event(
    legs: &[ConvertLeg],
    pairs: &[(U256, U256)],
) -> (Vec<(B256, U256)>, Vec<NoHolding>) {
    let mut merges = Vec::new();
    let mut holdings = Vec::new();

    for (leg, &(no_bal, yes_bal)) in legs.iter().zip(pairs) {
        if yes_bal > U256::ZERO
            && no_bal > U256::ZERO
            && let Some(cid) = leg.condition_id
        {
            merges.push((cid, yes_bal.min(no_bal)));
        }

        let remaining_no = no_bal.saturating_sub(yes_bal.min(no_bal));
        if remaining_no > U256::ZERO {
            holdings.push(NoHolding {
                question_id: leg.question_id,
                condition_id: leg.condition_id,
                remaining_no,
            });
        }
    }

    (merges, holdings)
}

/// Decompose remaining-NO holdings into convert tiers.
///
/// Distinct holding sizes `L1 < L2 < …` define the tiers: tier 1 converts
/// `L1` across every NO-holding leg, tier 2 converts `L2 − L1` across the
/// legs holding more than `L1`, and so on — the fixpoint a
/// read→convert→re-read loop converges to, but computable from one balance
/// snapshot because conditional-token math is deterministic.
///
/// A tier wider than `max_legs` is truncated to its first `max_legs` members
/// (input order): the convert then mints `amount` YES on each truncated-out
/// member, so cid-bearing ones get a compensating post-merge freeing the
/// same `$amount` the untruncated convert would have.
///
/// A lone-member tier is necessarily the last (membership shrinks as levels
/// rise); converting it frees 0 USDC, so it is kept for position hygiene
/// only when `amount ≥ single_leg_min_qty_raw`.
fn plan_tiers(
    holdings: &[NoHolding],
    single_leg_min_qty_raw: U256,
    max_legs: usize,
) -> Vec<ConvertTier> {
    let mut levels: Vec<U256> = holdings.iter().map(|h| h.remaining_no).collect();
    levels.sort_unstable();
    levels.dedup();

    let mut tiers = Vec::with_capacity(levels.len());
    let mut prev = U256::ZERO;
    for level in levels {
        let amount = level - prev;
        prev = level;
        let members: Vec<&NoHolding> = holdings
            .iter()
            .filter(|h| h.remaining_no >= level)
            .collect();
        if members.len() == 1 && amount < single_leg_min_qty_raw {
            break;
        }
        let question_ids = members
            .iter()
            .take(max_legs)
            .map(|h| h.question_id)
            .collect();
        let post_merges = members
            .iter()
            .skip(max_legs)
            .filter_map(|h| h.condition_id.map(|cid| (cid, amount)))
            .collect();
        tiers.push(ConvertTier {
            question_ids,
            amount,
            post_merges,
        });
    }
    tiers
}

/// Assemble an event's plan and compute the exact USDC.e it frees. `n_legs`
/// is the event's total leg count (drives the per-convert gas estimate).
fn plan_event(merges: Vec<(B256, U256)>, tiers: Vec<ConvertTier>, n_legs: usize) -> EventPlan {
    let mut proceeds = merges.iter().fold(U256::ZERO, |acc, &(_, a)| acc + a);
    for tier in &tiers {
        proceeds += U256::from(tier.question_ids.len().saturating_sub(1)) * tier.amount;
        proceeds = tier
            .post_merges
            .iter()
            .fold(proceeds, |acc, &(_, a)| acc + a);
    }
    EventPlan {
        merges,
        tiers,
        proceeds,
        n_legs,
    }
}

/// Compute an event's plan from a balance snapshot: per-leg `(no, yes)`
/// balance pairs in leg order (the pure entry into the planner for callers
/// bringing their own balance source).
#[must_use]
pub fn plan_event_from_balances(
    legs: &[ConvertLeg],
    pairs: &[(U256, U256)],
    single_leg_min_qty_raw: U256,
) -> EventPlan {
    let (merges, holdings) = classify_event(legs, pairs);
    let tiers = plan_tiers(&holdings, single_leg_min_qty_raw, MAX_LEGS_PER_CONVERT);
    plan_event(merges, tiers, legs.len())
}

/// Flatten per-event plans into one ordered, gas-weighted merge/convert call
/// list.
///
/// Order is load-bearing: each event's merges precede its converts (tier
/// amounts assume post-merge balances), and each tier's post-merges directly
/// follow that tier's convert (they consume the YES it mints). Calls execute
/// in order inside a relayer submission, and chunked submissions are
/// serialized, so the order survives chunking.
///
/// Every call here is also individually valid against the *pre-batch*
/// balance snapshot. Two batch shapes are known to be rejected by the
/// relayer's pre-submission simulation ("batch would revert"), both observed
/// live, and both avoided here:
/// - an in-batch wrap sized to include the batch's proceeds (the wrap
///   instead goes out as its own balance-read-sized submission after the
///   batch lands);
/// - a batch whose summed gas exceeds what one transaction can execute
///   (hence the gas weights here and the budget chunking in [`gas_chunks`]).
pub fn plan_calls(plans: &[EventPlan]) -> Result<Vec<PlannedCall>, Error> {
    let mut calls = Vec::new();

    for plan in plans {
        for call in merge_calls(&plan.merges) {
            calls.push(PlannedCall {
                call,
                gas: MERGE_GAS,
            });
        }
        for tier in &plan.tiers {
            calls.push(PlannedCall {
                call: DepositWalletCall {
                    target: NEG_RISK_COLLATERAL_ADAPTER,
                    data: build_convert_calldata(&tier.question_ids, tier.amount)?,
                },
                gas: convert_gas(plan.n_legs),
            });
            for call in merge_calls(&tier.post_merges) {
                calls.push(PlannedCall {
                    call,
                    gas: MERGE_GAS,
                });
            }
        }
    }

    Ok(calls)
}

/// Calldata for wrapping `amount` USDC.e into pUSD via the onramp, with an
/// unlimited-approve call prepended when `include_approve`.
#[must_use]
pub fn wrap_calls(wallet: Address, amount: U256, include_approve: bool) -> Vec<DepositWalletCall> {
    let mut calls = Vec::with_capacity(2);
    if include_approve {
        calls.push(DepositWalletCall {
            target: USDC_E,
            data: IErc20Ops::approveCall {
                spender: COLLATERAL_ONRAMP,
                value: U256::MAX,
            }
            .abi_encode(),
        });
    }
    calls.push(DepositWalletCall {
        target: COLLATERAL_ONRAMP,
        data: ICollateralOnrampOps::wrapCall {
            asset: USDC_E,
            to: wallet,
            amount,
        }
        .abi_encode(),
    });
    calls
}

